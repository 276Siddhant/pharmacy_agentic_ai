def call_chat(user_id: str, text: str) -> dict:
    """
    Calls your FastAPI /chat endpoint.
    """

    response = requests.post(
        f"{BACKEND_URL}/chat",
        params={
            "user_id": user_id,
            "message": text
        },
        timeout=TIMEOUT
    )

    response.raise_for_status()
    return response.json()


