def call_transcribe(audio_bytes: bytes) -> str:

    response = requests.post(
        f"{BACKEND_URL}/voice/transcribe",
        files={"audio": ("audio.wav", audio_bytes, "audio/wav")},
        timeout=TIMEOUT
    )

    response.raise_for_status()
    return response.json().get("text", "")


